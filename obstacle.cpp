Obstacle::Obstacle(Vector2 position) {
    this->position = position;
    

    for (unsigned int row = 0; row < grid.size(); ++row) {
        for (unsigned int column = 0; column < grid[0].size(); ++column) {
            if (grid[row][column] == 1) {
                float pos_x = position.x + column * 3; // x coordinate of block based on column number of grid
                float pos_y = position.y + row * 3;   // each block is 3 pixels tall, multiply row number by 3
                Block block = Block({pos_x, pos_y});
                blocks.push_back(block);
            }
        }
    }
}

void Obstacle::Draw() {
    for (auto& block : blocks) {
        block.Draw();
    }
}
